
public class RelationalOperators {

	public static void main(String[] args) {

		int i1=5;
		int i2=10;
		
		System.out.println(i1==i2);
		System.out.println(i1!=i2);
		System.out.println(i1>=i2);
		System.out.println(i1<=i2);
		
		
		double d1=20d;
		double d2=80d;
		
		double res1 = (d1+d2)*25;
		double res2 = res1%40;
		
		boolean res3 = res2<=20;
		System.out.println(res3);
		
				

	}

}
